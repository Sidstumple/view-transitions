const speakers = document.querySelectorAll('.speaker-card a')

speakers.forEach((speaker) => {
  speaker.addEventListener('click', () => {
    const card = speaker.closest('.speaker-card')
    const cardImage = card.querySelector('img')
    cardImage.style.viewTransitionClass = 'active-speaker'
  })
})







// Not related to view transitions, just making a navigation trigger:
const navMenu = document.querySelector('.navigation')
const navigationTrigger = document.querySelector('.navigation button')

if (navigationTrigger) {
  navigationTrigger.addEventListener('click', () => {
    navMenu.classList.toggle('is-active')
  })
}
